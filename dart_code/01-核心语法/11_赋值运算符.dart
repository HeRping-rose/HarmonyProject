void main() {
  // 等于 =
  int n1 = 10;

  // 加等于 +=
  n1 += 5;
  print(n1); // 15

  // 减等于 -=
  n1 -= 3;
  print(n1); // 12

  // 乘等于 *=
  n1 *= 2;
  print(n1); // 24

  // 除等于 /=
  // 注意：double类型的数据才能做除等于的操作
  double n2 = 5.0;
  n2 /= 2;
  print(n2); // 2.5

  // 取余等于 %=
  n1 %= 5;
  print(n1); // 4

  // 自增：在原有数值上加1 ++
  n1++;
  print(n1); // 5

  // 自减：在原有数值上减1 --
  n1--;
  print(n1); // 4
}
